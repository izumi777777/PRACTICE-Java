package practice.復習;

// else ifを使用して処理を複数に分岐する
public class SampleElseIf2 {
    public static void main(String[] args) {
            
            int n = 8;
            if(n%2 == 0)System.out.println("nは2の倍数です");
            if(n%3 == 0)System.out.println("nは3の倍数です");
            if(n%4 == 0)System.out.println("nは5の倍数です");
             
        
    }
    
}
