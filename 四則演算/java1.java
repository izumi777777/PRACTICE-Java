import java.util.*;
public class java1 {
    public static void main(String[] args) {
        // 足し算
        int a = 100;
        int b = 200;
        System.out.println(a+b);

        // 引き算
        System.out.println(a-b);

        // 掛け算を行う
        System.out.println(a*b);

        // 割り算を行う
        double x = 100;
        double y = 200;
        System.out.println(x/y);
    } 
}
